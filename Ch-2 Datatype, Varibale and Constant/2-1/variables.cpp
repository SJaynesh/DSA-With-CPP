#include <iostream>
using namespace std;

main()
{
    cout << "Datatype, Constant and Variable" << endl
         << endl;

    // Syntax : Datatype variableName;

    int num = 12;
    float PI = 3.14;
    char name = 'J';

    cout << "Number\t: " << num << "." << endl; // Number  : 12.
    cout << "PI\t: " << PI << endl;
    cout << "Name\t: " << name << endl;
}