#include <iostream>
using namespace std;

main()
{
    int a = 35, b = 5;

    cout << a << " + " << b << " = " << a + b << endl; // 35
    cout << a << " - " << b << " = " << a - b << endl; // 15
    cout << a << " * " << b << " = " << a * b << endl; // 250
    cout << a << " / " << b << " = " << a / b << endl; // 2
    cout << a << " % " << b << " = " << a % b << endl; // 5
}